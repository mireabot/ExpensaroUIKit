import UIKit

// MARK: Font sizes
public extension CGFloat {
  static var head1: CGFloat { CGFloat(42) } // Head1 Font size (Medium)
  static var head2: CGFloat { CGFloat(32) } // Head2 Font size (Medium)
  static var head3: CGFloat { CGFloat(24) } // Head3 Font size (Medium)
  static var title: CGFloat { CGFloat(18) } // Title Font size (Medium)
  static var largeBody: CGFloat { CGFloat(16) } // Large body Font size (Regular)
  static var body: CGFloat { CGFloat(15) } // Body Font size (Regular)
  static var small: CGFloat { CGFloat(13) } // Small Font size (Regular)
  static var overline: CGFloat { CGFloat(10) } // Overline Font size (Regular)
}
